					<h4>Crioterapia</h4>
					<div class="longdesc">
La crioterapia consiste en la aplicación del frío en la zona de adiposidad localizada para conseguir la degradación selectiva de las células grasas de una manera indolora, efectiva y duradera.
Las células grasas (adipocitos), son ricas en ácidos grasos saturados, lo que las hace especialmente más vulnerables al frío. Esta característica permite la degradación natural de los adipocitos sin dañar los tejidos contiguos al someterlos a bajas temperaturas. Una vez se produce la congelación de los adipocitos, estos entran en un proceso de disolución progresiva de la célula con liberación gradual de lípidos, siendo eliminados por el organismo en un periodo de entre 1 a 3 semanas, con la consiguiente reducción de volumen de la zona tratada.
combinación de secuencias con masaje vibratorio y drenaje linfático

